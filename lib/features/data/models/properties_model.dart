import 'package:earthquake_app/features/domain/entities/properties_entity.dart';

class PropertiesModel extends PropertiesEntity {
  PropertiesModel(
      {required mag,
      required place,
      required time,
      required updated,
      required tz,
      required url,
      required detail,
      required felt,
      required cdi,
      required mmi,
      required alert,
      required status,
      required tsunami,
      required sig,
      required net,
      required code,
      required ids,
      required sources,
      required types,
      required nst,
      required dmin,
      required rms,
      required gap,
      required magType,
      required type,
      required title})
      : super(
            mag: mag,
            place: place,
            time: time,
            updated: updated,
            tz: tz,
            url: url,
            detail: detail,
            felt: felt,
            cdi: cdi,
            mmi: mmi,
            alert: alert,
            status: status,
            tsunami: tsunami,
            sig: sig,
            net: net,
            code: code,
            ids: ids,
            sources: sources,
            types: types,
            nst: nst,
            dmin: dmin,
            rms: rms,
            gap: gap,
            magType: magType,
            type: type,
            title: title);

  factory PropertiesModel.fromJson(Map<String, dynamic> json) =>
      PropertiesModel(
          mag: json['mag'],
          place: json['place'],
          time: json['time'],
          updated: json['updated'],
          tz: json['tz'],
          url: json['url'],
          detail: json['detail'],
          felt: json['felt'],
          cdi: json['cdi'],
          mmi: json['mmi'],
          alert: json['alert'],
          status: json['status'],
          tsunami: json['tsunami'],
          sig: json['sig'],
          net: json['net'],
          code: json['code'],
          ids: json['ids'],
          sources: json['sources'],
          types: json['types'],
          nst: json['nst'],
          dmin: json['dmin'],
          rms: json['rms'],
          gap: json['gap'],
          magType: json['magType'],
          type: json['type'],
          title: json['title']);

  Map<String, dynamic> toJson() => {
        'mag': this.mag,
        'place': this.place,
        'time': this.time,
        'updated': this.updated,
        'tz': this.tz,
        'url': this.url,
        'detail': this.detail,
        'felt': this.felt,
        'cdi': this.cdi,
        'mmi': this.mmi,
        'alert': this.alert,
        'status': this.status,
        'tsunami': this.tsunami,
        'sig': this.sig,
        'net': this.net,
        'code': this.code,
        'ids': this.ids,
        'sources': this.sources,
        'types': this.types,
        'nst': this.nst,
        'dmin': this.dmin,
        'rms': this.rms,
        'gap': this.gap,
        'magType': this.magType,
        'type': this.type,
        'title': this.title
      };
}
